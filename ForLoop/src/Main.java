public class Main {

    public static void main(String[] args) {

        // for loop = executes a block of code a limited amount of times

        for(int i=10; i>=0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy new year!");

        for(int x=1; x<=14; x++){
            System.out.println(x);
        }

    }
}